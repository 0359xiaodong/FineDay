package com.gitonway.fineday.domain.model;

public class WeaterObjModel {
	
	/**
	 * sun天气类型
	 */
	private String typeSun;
	/**
	 * moon天气类型
	 */
	private String typeMoon;
	
	/**
	 * 时间
	 */
	private String date;
	
	/**
	 * sun天气
	 */
	private String wcSun;
	/**
	 * moon天气
	 */
	private String wcMoon;
	/**
	 * 最高温度
	 */
	private String tempH;
	/**
	 * 最低温度
	 */
	private String tempL;
	/**
	 * sun风向
	 */
	private String windSun;
	/**
	 * moon风向
	 */
	private String windMoon;
	/**
	 * sun风力
	 */
	private String windPowerSun;
	/**
	 * moon风力
	 */
	private String windPowerMoon;
	

	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTypeSun() {
		return typeSun;
	}
	public void setTypeSun(String typeSun) {
		this.typeSun = typeSun;
	}
	public String getTypeMoon() {
		return typeMoon;
	}
	public void setTypeMoon(String typeMoon) {
		this.typeMoon = typeMoon;
	}
	public String getWcSun() {
		return wcSun;
	}
	public void setWcSun(String wcSun) {
		this.wcSun = wcSun;
	}
	public String getWcMoon() {
		return wcMoon;
	}
	public void setWcMoon(String wcMoon) {
		this.wcMoon = wcMoon;
	}
	public String getTempH() {
		return tempH;
	}
	public void setTempH(String tempH) {
		this.tempH = tempH;
	}
	public String getTempL() {
		return tempL;
	}
	public void setTempL(String tempL) {
		this.tempL = tempL;
	}
	public String getWindSun() {
		return windSun;
	}
	public void setWindSun(String windSun) {
		this.windSun = windSun;
	}
	public String getWindMoon() {
		return windMoon;
	}
	public void setWindMoon(String windMoon) {
		this.windMoon = windMoon;
	}
	public String getWindPowerSun() {
		return windPowerSun;
	}
	public void setWindPowerSun(String windPowerSun) {
		this.windPowerSun = windPowerSun;
	}
	public String getWindPowerMoon() {
		return windPowerMoon;
	}
	public void setWindPowerMoon(String windPowerMoon) {
		this.windPowerMoon = windPowerMoon;
	}
	
	
	

}
